export type LiveAssistantConversationTurn = {
  id: string;
  prompt: string;
  expectedMode: LiveAssistantExpectedMode;
  expectedAnswerModes?: string[];
  expectedRoutePriority?: string | string[];
  expectedConversationRewrite?: boolean;
  expectedMustInclude: string[];
  expectedMustNotInclude: string[];
  maxWords?: number;
  shouldAskFollowUp?: boolean;
  notes?: string;
};

export type LiveAssistantConversationScenario = {
  id: string;
  title: string;
  category: string;
  turns: LiveAssistantConversationTurn[];
};

const GLOBAL_CONVERSATION_FORBIDDEN = [
  'chain-of-thought',
  'hidden scratchpad',
  'step 1:',
  'step 2:',
  "I don't have a safe Veranote answer",
  'I do not have a confident medication match',
  'Get the source in cleanly',
  'WellSky output in view',
];

function withGlobalForbidden(turn: Omit<LiveAssistantConversationTurn, 'expectedMustNotInclude'> & {
  expectedMustNotInclude?: string[];
}): LiveAssistantConversationTurn {
  return {
    ...turn,
    expectedMustNotInclude: [
      ...GLOBAL_CONVERSATION_FORBIDDEN,
      ...(turn.expectedMustNotInclude || []),
    ],
  };
}

