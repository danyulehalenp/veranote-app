function createCases(
  batchId: LiveAssistantStagedBatchId,
  prefix: string,
  seeds: CaseSeed[],
): StagedLiveAssistantQuestionCase[] {
  return seeds.map((seed, index) => ({
    id: `${prefix}-${String(index + 1).padStart(3, '0')}`,
    batchId,
    question: seed.question,
    expectedLane: seed.expectedLane ?? seed.expectedMode,
    category: seed.category,
    sourceSection: seed.sourceSection,
    expectedMode: seed.expectedMode,
    expectedMustInclude: seed.expectedMustInclude ?? defaultMustInclude(seed.expectedMode, seed.category),
    expectedMustNotInclude: seed.expectedMustNotInclude ?? defaultMustNotInclude(seed.expectedMode),
    maxWords: seed.maxWords ?? defaultMaxWords(seed.expectedMode),
    shouldAskFollowUp: seed.shouldAskFollowUp ?? defaultShouldAskFollowUp(seed.expectedMode, seed.safetyLevel ?? 'routine'),
    safetyLevel: seed.safetyLevel ?? defaultSafetyLevel(seed.expectedMode),
    notes: seed.notes,
    recentMessages: seed.recentMessages,
  }));
}

function defaultMustInclude(expectedMode: LiveAssistantExpectedMode, category: string) {
  if (expectedMode === 'approval_indication' || category.includes('FDA')) {
    return ['FDA', 'approved', 'label'];
  }

  if (expectedMode === 'interaction_safety') {
    return ['risk', 'verify'];
  }

  if (expectedMode === 'lab_monitoring') {
    return ['monitor', 'current prescribing'];
  }

  if (expectedMode === 'urgent_safety') {
    return ['urgent', 'local protocol'];
  }

  if (expectedMode === 'diagnostic_summary') {
    return ['summary', 'diagnostic'];
  }

  return [];
}

function defaultMustNotInclude(expectedMode: LiveAssistantExpectedMode) {
  if (expectedMode === 'interaction_safety') {
    return INTERACTION_FORBIDDEN;
  }

  if (expectedMode === 'urgent_safety') {
    return URGENT_FORBIDDEN;
  }

  if (expectedMode === 'diagnostic_summary') {
    return DSM_FORBIDDEN;
  }

  if (expectedMode === 'applied_clinical' || expectedMode === 'lab_monitoring') {
    return APPLIED_FORBIDDEN;
  }

  return ROUTINE_REFERENCE_FORBIDDEN;
}

function defaultMaxWords(expectedMode: LiveAssistantExpectedMode) {
  if (expectedMode === 'pure_reference' || expectedMode === 'simple_factual') {
    return 90;
  }

  if (expectedMode === 'urgent_safety') {
    return 140;
  }

  if (expectedMode === 'diagnostic_summary') {
    return 160;
  }

  return 120;
}

function defaultShouldAskFollowUp(expectedMode: LiveAssistantExpectedMode, safetyLevel: LiveAssistantSafetyLevel) {
  if (expectedMode === 'pure_reference' || expectedMode === 'simple_factual' || expectedMode === 'approval_indication' || expectedMode === 'diagnostic_summary') {
    return false;
  }

  return safetyLevel !== 'urgent';
}

function defaultSafetyLevel(expectedMode: LiveAssistantExpectedMode): LiveAssistantSafetyLevel {
  if (expectedMode === 'urgent_safety') {
    return 'urgent';
  }

  if (expectedMode === 'diagnostic_summary') {
    return 'diagnostic_reference';
  }

  if (expectedMode === 'interaction_safety' || expectedMode === 'lab_monitoring' || expectedMode === 'applied_clinical') {
    return 'caution';
  }

  return 'routine';
}

