import type { LiveAssistantExpectedMode, LiveAssistantQuestionCase } from '@/lib/eval/live-assistant/live-assistant-question-bank';

export type LiveAssistantStagedBatchId =
  | 'batch1-medication-reference'
  | 'batch2-fda-approvals-indications'
  | 'batch3-interactions-contraindications'
  | 'batch4-labs-monitoring'
  | 'batch5-emergency-toxicology-withdrawal'
  | 'batch6-diagnostic-general-concepts'
  | 'diagnostic-adversarial'
  | 'route-boundary-stress';

export type LiveAssistantSafetyLevel = NonNullable<LiveAssistantQuestionCase['safetyLevel']>;

export type StagedLiveAssistantQuestionCase = LiveAssistantQuestionCase & {
  batchId: LiveAssistantStagedBatchId;
  category: string;
  sourceSection: string;
  safetyLevel: LiveAssistantSafetyLevel;
};

type CaseSeed = {
  question: string;
  expectedLane?: string;
  category: string;
  sourceSection: string;
  expectedMode: LiveAssistantExpectedMode;
  safetyLevel?: LiveAssistantSafetyLevel;
  expectedMustInclude?: string[];
  expectedMustNotInclude?: string[];
  maxWords?: number;
  shouldAskFollowUp?: boolean;
  notes?: string;
  recentMessages?: LiveAssistantQuestionCase['recentMessages'];
};

