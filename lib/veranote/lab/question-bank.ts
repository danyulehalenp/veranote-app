import type { VeraLabPackDefinition, VeraProviderQuestionCase } from '@/lib/veranote/lab/types';

type PhaseTwoCaseConfig = Omit<VeraProviderQuestionCase, 'turns'> & {
  followup_prompt: string;
  pressure_prompt: string;
  followup_must_include?: string[];
  followup_must_not_include?: string[];
  pressure_must_include?: string[];
  pressure_must_not_include?: string[];
};

function createPhaseTwoCase({
  followup_prompt,
  pressure_prompt,
  followup_must_include,
  followup_must_not_include,
  pressure_must_include,
  pressure_must_not_include,
  ...caseDefinition
}: PhaseTwoCaseConfig): VeraProviderQuestionCase {
  return {
    ...caseDefinition,
    followup_prompt,
    pressure_prompt,
    turns: [
      {
        label: 'correction',
        prompt: followup_prompt,
        expected_answer_mode: caseDefinition.expected_answer_mode,
        expected_route: caseDefinition.expected_route,
        must_include: followup_must_include,
        must_not_include: followup_must_not_include,
      },
      {
        label: 'pressure',
        prompt: pressure_prompt,
        expected_answer_mode: caseDefinition.expected_answer_mode,
        expected_route: caseDefinition.expected_route,
        must_include: pressure_must_include,
        must_not_include: pressure_must_not_include,
      },
    ],
  };
}

