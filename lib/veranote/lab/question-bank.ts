export const allVeraProviderQuestionCases = veraProviderQuestionPacks.flatMap((pack) => pack.cases);

export function selectVeraProviderQuestionCases(packIds?: string[], categories?: string[]) {
  const normalizedPackIds = new Set((packIds || []).filter(Boolean));
  const normalizedCategories = new Set((categories || []).filter(Boolean));

  return allVeraProviderQuestionCases.filter((item) => {
    const packMatch = !normalizedPackIds.size || veraProviderQuestionPacks.some((pack) => (
      normalizedPackIds.has(pack.id) && pack.cases.some((packCase) => packCase.id === item.id)
    ));
    const categoryMatch = !normalizedCategories.size || normalizedCategories.has(item.category);
    return packMatch && categoryMatch;
  });
}
